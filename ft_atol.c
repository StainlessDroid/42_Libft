#include "libft.h"

long	ft_atol(const char *str)
{
	int		sign;
	long	nbr;

	nbr = 0;
	sign = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		sign = -1;
		str++;
	}
	while (ft_isdigit(*str))
	{
		nbr *= 10;
		nbr += *str - '0';
		if (nbr > INT_MAX || nbr < INT_MIN)
			return (nbr);
		str++;
	}
	return (nbr * sign);
}
